#include "libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*new_list;
	t_list	*first_elem;

	if (!lst)
		return (NULL);
	new_list = f(lst);
	first_elem = new_list;
	while (lst->next)
	{
		new_list->next = f(lst->next);
		new_list = new_list->next;
		lst = lst->next;
	}
	return (first_elem);
}
