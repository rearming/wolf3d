#include "libft.h"

char		*ft_strljoin(const char *s1, const char *s2, size_t size)
{
	char	*res;
	size_t	i;

	i = 0;
	if (!s1 || !s2)
		return (NULL);
	if (!(res = (char*)malloc(sizeof(char) * size + 1)))
		return (NULL);
	while (*s1)
	{
		res[i] = *s1;
		s1++;
		i++;
	}
	while (*s2)
	{
		res[i] = *s2;
		s2++;
		i++;
	}
	res[i] = '\0';
	return (res);
}
