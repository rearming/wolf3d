#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	unsigned int nbr;

	if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		nbr = -1 * nb;
	}
	else
		nbr = nb;
	if (nbr >= 10)
		ft_putnbr_fd(nbr / 10, fd);
	ft_putchar_fd((nbr % 10 + 48), fd);
}
